CREATE TABLE TOPICS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    SLUG VARCHAR2(200) NOT NULL,
    TITLE VARCHAR2(500) NOT NULL,
    DESCRIPTION CLOB,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT UQ_TOPICS_SLUG UNIQUE (SLUG)
);

CREATE TABLE CONCEPTS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TOPIC_ID NUMBER NOT NULL,
    SLUG VARCHAR2(200) NOT NULL,
    LABEL VARCHAR2(500) NOT NULL,
    SUMMARY CLOB,
    PRIMARY_TOPIC_SLUG VARCHAR2(200) NOT NULL,
    RELATED_TOPICS_JSON CLOB,
    FOUNDATIONAL NUMBER(1) DEFAULT 0 NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT FK_CONCEPTS_TOPIC FOREIGN KEY (TOPIC_ID) REFERENCES TOPICS (ID),
    CONSTRAINT UQ_CONCEPTS_TOPIC_SLUG UNIQUE (TOPIC_ID, SLUG)
);

CREATE TABLE CONCEPT_EDGES (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    SOURCE_CONCEPT_ID NUMBER NOT NULL,
    TARGET_CONCEPT_ID NUMBER NOT NULL,
    EDGE_KIND VARCHAR2(100) NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT FK_CONCEPT_EDGES_SOURCE FOREIGN KEY (SOURCE_CONCEPT_ID) REFERENCES CONCEPTS (ID),
    CONSTRAINT FK_CONCEPT_EDGES_TARGET FOREIGN KEY (TARGET_CONCEPT_ID) REFERENCES CONCEPTS (ID),
    CONSTRAINT UQ_CONCEPT_EDGES UNIQUE (SOURCE_CONCEPT_ID, TARGET_CONCEPT_ID, EDGE_KIND)
);

CREATE TABLE RESEARCH_REPORTS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TOPIC_ID NUMBER NOT NULL,
    TITLE VARCHAR2(500) NOT NULL,
    SOURCE_PATH VARCHAR2(1000),
    VERSION_NUMBER NUMBER DEFAULT 1 NOT NULL,
    CONTENT CLOB NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT FK_RESEARCH_REPORTS_TOPIC FOREIGN KEY (TOPIC_ID) REFERENCES TOPICS (ID),
    CONSTRAINT UQ_RESEARCH_REPORTS_VERSION UNIQUE (TOPIC_ID, VERSION_NUMBER)
);

CREATE TABLE RESEARCH_CHUNKS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    REPORT_ID NUMBER NOT NULL,
    TOPIC_ID NUMBER NOT NULL,
    CHUNK_INDEX NUMBER NOT NULL,
    CHUNK_TEXT CLOB NOT NULL,
    SOURCE_HEADING VARCHAR2(500),
    SOURCE_REFERENCE VARCHAR2(1000),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT FK_RESEARCH_CHUNKS_REPORT FOREIGN KEY (REPORT_ID) REFERENCES RESEARCH_REPORTS (ID),
    CONSTRAINT FK_RESEARCH_CHUNKS_TOPIC FOREIGN KEY (TOPIC_ID) REFERENCES TOPICS (ID),
    CONSTRAINT UQ_RESEARCH_CHUNKS_INDEX UNIQUE (REPORT_ID, CHUNK_INDEX)
);

CREATE TABLE CONCEPT_CHUNK_LINKS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CONCEPT_ID NUMBER NOT NULL,
    CHUNK_ID NUMBER NOT NULL,
    SNIPPET_TEXT CLOB NOT NULL,
    SNIPPET_ORDER NUMBER DEFAULT 0 NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT FK_CONCEPT_CHUNK_LINKS_CONCEPT FOREIGN KEY (CONCEPT_ID) REFERENCES CONCEPTS (ID),
    CONSTRAINT FK_CONCEPT_CHUNK_LINKS_CHUNK FOREIGN KEY (CHUNK_ID) REFERENCES RESEARCH_CHUNKS (ID),
    CONSTRAINT UQ_CONCEPT_CHUNK_LINKS UNIQUE (CONCEPT_ID, CHUNK_ID, SNIPPET_ORDER)
);

CREATE TABLE XP_SESSIONS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    SESSION_KEY VARCHAR2(200) NOT NULL,
    TOPIC_ID NUMBER NOT NULL,
    REPORT_ID NUMBER,
    STATUS VARCHAR2(100) DEFAULT 'created' NOT NULL,
    GROUNDING_LABEL VARCHAR2(500),
    STARTED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    FINISHED_AT TIMESTAMP,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT FK_XP_SESSIONS_TOPIC FOREIGN KEY (TOPIC_ID) REFERENCES TOPICS (ID),
    CONSTRAINT FK_XP_SESSIONS_REPORT FOREIGN KEY (REPORT_ID) REFERENCES RESEARCH_REPORTS (ID),
    CONSTRAINT UQ_XP_SESSIONS_KEY UNIQUE (SESSION_KEY)
);

CREATE TABLE XP_TURNS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    SESSION_ID NUMBER NOT NULL,
    TURN_INDEX NUMBER NOT NULL,
    ROLE VARCHAR2(50) NOT NULL,
    PHASE VARCHAR2(100),
    TARGET_CONCEPT_SLUGS CLOB,
    CONTENT CLOB NOT NULL,
    HINTS_USED NUMBER DEFAULT 0 NOT NULL,
    GROUNDING_REFERENCE VARCHAR2(500),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT FK_XP_TURNS_SESSION FOREIGN KEY (SESSION_ID) REFERENCES XP_SESSIONS (ID),
    CONSTRAINT UQ_XP_TURNS_INDEX UNIQUE (SESSION_ID, TURN_INDEX)
);

CREATE TABLE CONCEPT_ASSESSMENTS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    SESSION_ID NUMBER NOT NULL,
    CONCEPT_ID NUMBER NOT NULL,
    SCORE_BEFORE NUMBER(5,2) NOT NULL,
    SCORE_AFTER NUMBER(5,2) NOT NULL,
    BAND_BEFORE VARCHAR2(50),
    BAND_AFTER VARCHAR2(50) NOT NULL,
    DELTA NUMBER(5,2) NOT NULL,
    TREND VARCHAR2(20) NOT NULL,
    NEXT_STEP VARCHAR2(1000),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT FK_CONCEPT_ASSESSMENTS_SESSION FOREIGN KEY (SESSION_ID) REFERENCES XP_SESSIONS (ID),
    CONSTRAINT FK_CONCEPT_ASSESSMENTS_CONCEPT FOREIGN KEY (CONCEPT_ID) REFERENCES CONCEPTS (ID)
);

CREATE TABLE CONCEPT_EVIDENCE (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ASSESSMENT_ID NUMBER NOT NULL,
    CONCEPT_ID NUMBER NOT NULL,
    EVIDENCE_KIND VARCHAR2(100) NOT NULL,
    EVIDENCE_SNIPPET CLOB NOT NULL,
    HINT_USED NUMBER(1) DEFAULT 0 NOT NULL,
    CONFUSION_NOTE VARCHAR2(1000),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT FK_CONCEPT_EVIDENCE_ASSESSMENT FOREIGN KEY (ASSESSMENT_ID) REFERENCES CONCEPT_ASSESSMENTS (ID),
    CONSTRAINT FK_CONCEPT_EVIDENCE_CONCEPT FOREIGN KEY (CONCEPT_ID) REFERENCES CONCEPTS (ID)
);

CREATE INDEX IDX_TOPICS_SLUG ON TOPICS (SLUG);
CREATE INDEX IDX_CONCEPTS_TOPIC ON CONCEPTS (TOPIC_ID);
CREATE INDEX IDX_CONCEPTS_SLUG ON CONCEPTS (SLUG);
CREATE INDEX IDX_RESEARCH_REPORTS_TOPIC ON RESEARCH_REPORTS (TOPIC_ID);
CREATE INDEX IDX_RESEARCH_CHUNKS_REPORT ON RESEARCH_CHUNKS (REPORT_ID);
CREATE INDEX IDX_CONCEPT_CHUNK_LINKS_CONCEPT ON CONCEPT_CHUNK_LINKS (CONCEPT_ID);
CREATE INDEX IDX_XP_SESSIONS_TOPIC ON XP_SESSIONS (TOPIC_ID);
CREATE INDEX IDX_XP_TURNS_SESSION ON XP_TURNS (SESSION_ID);
CREATE INDEX IDX_CONCEPT_ASSESSMENTS_CONCEPT ON CONCEPT_ASSESSMENTS (CONCEPT_ID);
CREATE INDEX IDX_CONCEPT_EVIDENCE_CONCEPT ON CONCEPT_EVIDENCE (CONCEPT_ID);
